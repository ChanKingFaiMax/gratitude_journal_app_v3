/**
 * Get a random spiritual master from the 4 selected guides
 */
export function getRandomMaster(): SpiritualMaster {
  const randomIndex = Math.floor(Math.random() * SPIRITUAL_MASTERS.length);
  return SPIRITUAL_MASTERS[randomIndex];
}

/**
 * Get master by ID
 */
export function getMasterById(id: string): SpiritualMaster | undefined {
  return SPIRITUAL_MASTERS.find(m => m.id === id);
}
